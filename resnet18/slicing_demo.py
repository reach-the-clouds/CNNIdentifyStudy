
import numpy as np

a = np.arange(15)
print("完整数组:", a)
print("数组长度:", len(a))

# 基本切片示例
print("\n切片示例:")
print("a[2:5] =", a[2:5])  # 包含索引2、3、4，不包含5
print("a[0:3] =", a[0:3])  # 包含索引0、1、2，不包含3
print("a[5:] =", a[5:])    # 从索引5开始到末尾
print("a[:5] =", a[:5])    # 从开始到索引5（不包含5）

# 使用负索引
print("\n负索引切片:")
print("a[-3:] =", a[-3:])  # 最后3个元素
print("a[:-3] =", a[:-3])  # 除了最后3个元素的所有元素

# 步长切片
print("\n步长切片:")
print("a[1:10:2] =", a[1:10:2])  # 从索引1到9，步长为2
print("a[::3] =", a[::3])        # 整个数组，步长为3
